async function fetchJson(path) {
  const res = await fetch(SUPABASE_URL + "/rest/v1/" + path, { headers: HEADERS });
  if (!res.ok) throw new Error(path + " " + res.status + " " + (await res.text()));
  return res.json();
}

async function insertRows(rows) {
  const res = await fetch(SUPABASE_URL + "/rest/v1/libraly_app", {
    method: "POST",
    headers: HEADERS,
    body: JSON.stringify(rows),
  });
  if (!res.ok) throw new Error("INSERT " + res.status + " " + (await res.text()));
}

async function main() {
  if (BOOKS.length !== 100) {
    throw new Error("BOOKS 件数が 100 ではありません: " + BOOKS.length);
  }

  const [categories, existing] = await Promise.all([
    fetchJson("libraly_app_category?select=id,name"),
    fetchJson("libraly_app?select=title"),
  ]);

  const catMap = {};
  categories.forEach((c) => {
    catMap[c.name] = c.id;
  });

  const existingTitles = new Set(existing.map((b) => b.title));
  const rows = [];
  const skipped = [];

  for (const [title, content, author, catName] of BOOKS) {
    if (existingTitles.has(title)) {
      skipped.push(title);
      continue;
    }
    const category_id = catMap[catName];
    if (!category_id) throw new Error("カテゴリ不明: " + catName);
    rows.push({ title, content, name: author, category_id });
  }

  if (rows.length === 0) {
    console.log("追加対象なし（すべて登録済み）");
    return;
  }

  const batchSize = 25;
  for (let i = 0; i < rows.length; i += batchSize) {
    await insertRows(rows.slice(i, i + batchSize));
  }

  const total = await fetchJson("libraly_app?select=id");
  console.log("OK: " + rows.length + " 冊追加（スキップ " + skipped.length + " 冊）");
  console.log("図書マスタ合計: " + total.length + " 冊");
  if (skipped.length) console.log("スキップ:", skipped.join(", "));
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
